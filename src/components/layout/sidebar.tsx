export function Sidebar() {
  const pathname = usePathname();
  const { user } = useAuthStore();

  // 获取用户角色和权限
  const userRole = (user?.role || "user") as UserRole;
  const userPermissions = (user?.permissions || []) as ModulePermission[];

  // 判断是否可以访问用户管理
  const canAccessUserManagement = hasPermission(
    userRole,
    userPermissions,
    "userManagement",
  );

  // 过滤有权限的导航项
  const filteredNavItems = navItems.filter((item) =>
    hasPermission(userRole, userPermissions, item.permission),
  );

  return (
    <aside className="hidden lg:flex lg:flex-col lg:w-64 lg:fixed lg:inset-y-0 lg:pt-16 lg:border-r lg:border-[var(--border)] lg:bg-[var(--card)]/80 lg:backdrop-blur-md">
      <nav className="flex-1 px-3 py-6 space-y-1">
        {filteredNavItems.map((item) => {
          const isActive =
            pathname === item.href || pathname.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex items-center gap-3 px-3 py-2.5 rounded text-sm font-medium transition-all",
                isActive
                  ? "bg-gradient-to-r from-[var(--primary)]/20 to-[var(--secondary)]/20 text-[var(--primary)] border-l-2 border-[var(--primary)] shadow-[0_0_15px_var(--glow)]"
                  : "text-[var(--foreground)] opacity-70 hover:opacity-100 hover:bg-[var(--muted)] hover:text-[var(--primary)]",
              )}
            >
              {item.icon}
              {item.title}
            </Link>
          );
        })}

        {/* 用户管理 - 管理员和超管可见 */}
        {canAccessUserManagement && (
          <Link
            href="/users"
            className={cn(
              "flex items-center gap-3 px-3 py-2.5 rounded text-sm font-medium transition-all",
              pathname === "/users" || pathname.startsWith("/users/")
                ? "bg-gradient-to-r from-[var(--primary)]/20 to-[var(--secondary)]/20 text-[var(--primary)] border-l-2 border-[var(--primary)] shadow-[0_0_15px_var(--glow)]"
                : "text-[var(--foreground)] opacity-70 hover:opacity-100 hover:bg-[var(--muted)] hover:text-[var(--primary)]",
            )}
          >
            <svg
              className="h-5 w-5"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M12 4.354a4 4 0 110 5.292M15 21H3v-1a6 6 0 0112 0v1zm0 0h6v-1a6 6 0 00-9-5.197M13 7a4 4 0 11-8 0 4 4 0 018 0z"
              />
            </svg>
            用户管理
          </Link>
        )}
      </nav>
    </aside>
  );
}
